    public static String saveotherBitmap(Bitmap bitmap, String imgurl, Context mContext) throws Exception {
    	String bitmapName = imgurl.substring(imgurl.lastIndexOf("/") + 1); //传入一个远程图片的url，然后取最后的图片名字
    	String filePath = Environment.getExternalStorageDirectory()+"/Time/"+bitmapName;
    	File file = new File(filePath);
    	if(file.exists()){
    		file.delete();
    	}
        OutputStream output = new FileOutputStream(filePath);
        bitmap.compress(CompressFormat.PNG, 100, output);
        output.flush();
        output.close();
        return filePath;
    }
